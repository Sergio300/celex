                <p align="center">
                    El Programa Delfín, se creó en 1995 con el objetivo fortalecer la cultura de colaboración entre las Instituciones de Educación Superior y Centros de Investigación integrantes del Programa, a través de la movilidad de profesores-investigadores, estudiantes y de la divulgación de productos científicos y tecnológicos; en lo particular para fortalecer el desarrollo de la investigación y el posgrado nacional.

Específicamente, se promueve la movilidad estudiantil mediante estancias académicas de investigación, en el marco del Verano de la Investigación Científica y Tecnológica del Pacífico. Este programa de movilidad, fortalece la vocación de los jóvenes por la ciencia y la tecnología e influye en su decisión por integrarse a programas de posgrado en el país y el extranjero.

Este programa está dirigido a estudiantes de excelencia académica a partir de cuarto semestre, tengan un promedio mínimo de 9.0 y cuenten con talento en las actividades relacionadas con la ciencia y la tecnología, con el propósito de fortalecer su vocación por la investigación científica y el desarrollo tecnológico.

Los estudiantes seleccionados estarán realizando una estancia de dos meses (de junio a agosto) en algún centro de investigación del D.F y área metropolitana o bien al interior del país en donde se integran a proyectos en desarrollo, asesorados por distinguidos investigadores.

Al término de la estancia, los estudiantes participantes expondrán el trabajo realizado en un Congreso Nacional que se lleva a cabo en la penúltima semana de agosto.

Este programa facilita la movilidad estudiantil en los diferentes centros de investigación a lo largo y ancho de nuestro país. ¡Te invitamos a participar, no pierdas la oportunidad!

Consulta más información sobre este programa en el sitio web de la Dirección de Educación Media Superior (www.dems.ipn.mx) o en la página del Programa Delfín (www.programadelfin.com.mx).

